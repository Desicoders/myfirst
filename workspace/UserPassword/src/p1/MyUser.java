package p1;

public class MyUser {
	
			private int id;
			private String name;
			
			MyUser(){}
			
			public int getId() {
				return id;
			}
			public void setId(int id) {
				this.id = id;
			}
			public String getName() {
				return name;
			}
			public void setName(String name) {
				this.name = name;
			}
			
			

}
